    private void ViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButtonActionPerformed
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/medicine","root","");
           
            String query = "select *from medicinerecord where Name='"+NameField.getText()+"'";
            
            PreparedStatement statement = (PreparedStatement) connection.prepareStatement(query);
            ResultSet set = statement.executeQuery();
            
            while(set.next())
            {
                NameField.setText(set.getString("Name"));
                LocationField.setText(set.getString("Location"));
                AvilableField.setText(set.getString("Avalible"));
                GroupField.setText(set.getString("Grup"));
                PriceField.setText(set.getString("Price"));
            }
            
            
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error!"+ex);
        
        }
        
    }//GEN-LAST:event_ViewButtonActionPerformed


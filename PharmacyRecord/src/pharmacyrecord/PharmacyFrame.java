            Statement statement = (Statement) connection.createStatement();
            String query = "insert into medicinerecord(Name,Location,Avalible,Grup,Price)values('"+NameField.getText()+"','"+LocationField.getText()+"','"+AvilableField.getText()+"','"+GroupField.getText()+"','"+PriceField.getText()+"') ";
            
